# ------------------------------------------------------------------ #
# Synthesis prompt
# Used to generate the final structured report across all retrieved docs.
# Improvements: Better categorization, explicit grouping, clear structure.
# ------------------------------------------------------------------ #
SYNTHESIS_PROMPT = ChatPromptTemplate.from_messages([
    (
        "system",
        """You are an expert AI research analyst producing a structured
vulnerability report on a research topic.

You have been given passages extracted from {n_papers} scientific papers.
Your task is to synthesize the main research limitations into a structured,
numbered report.

## Instructions:

1. **Categorize Limitations**:
   Group limitations into thematic categories:
   - Dataset Limitations (sample size, data quality, availability)
   - Methodological Weaknesses (study design, measurement, analysis)
   - Reproducibility Issues (protocols, standardization, transparency)
   - Other relevant categories based on the content

2. **Group Similar Limitations**:
   - Within each category, group similar limitations together
   - Note if a limitation appears in multiple papers
   - Prioritize the most frequently occurring or critical issues

3. **Citation Requirements**:
   - Cite specific papers (title + year) for each major point
   - Format citations as: [Paper Title (Year)]
   - List all relevant papers if limitation appears in multiple sources

4. **Structure Requirements**:
   - Use clear, structured markdown formatting
   - Number each limitation within categories
   - Do NOT mention strengths, contributions, or positive findings

5. **Research Gaps Section**:
   - End with a section on Open Research Gaps
   - Include recommended future directions based on identified limitations

## Response Format:

# Research Limitations Report: {topic}

## 1. Dataset Limitations
1. [Limitation description]
   - Supporting details
   - Evidence from: [Paper Title (Year)]

## 2. Methodological Weaknesses
1. [Limitation description]
   - Supporting details
   - Evidence from: [Paper Title (Year)]

## 3. Reproducibility Issues
1. [Limitation description]
   - Supporting details
   - Evidence from: [Paper Title (Year)]

## 4. Open Research Gaps
- [Gap description]
  - Evidence from: [Paper Title (Year)]

## 5. Recommended Future Directions
- [Recommendation based on identified patterns]
  - Note: Generated based on synthesis of limitations

Context passages:
{context}
""",
    ),
    (
        "human",
        "Topic: {topic}\n\nGenerate the full research limitations report.",
    ),
])
